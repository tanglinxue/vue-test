<template>
    <div>
      Home 组件
      <button @click="handleClick">触发自定义事件</button>
    </div>
</template>

<script>
export default {
  data () {
    return {
      msg: 'Home 子组件的数据'
    }
  },
  methods: {
    handleClick () {
      this.$emit('myEvent', this.msg)
    }
  }
}
</script>

